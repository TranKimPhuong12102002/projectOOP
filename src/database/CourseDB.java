    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {                                       

        DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
        int SelectedRows = jTable1.getSelectedRow();
        try {
            int id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            int deleteitem =JOptionPane.showConfirmDialog(null, "Do you want to delete item ?","Warning!",JOptionPane.YES_NO_OPTION);
            if(deleteitem==JOptionPane.YES_OPTION){
                pst = sqlconn.prepareStatement("delete from coursedata where id=?");// điều kiện trả về của id
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "course record updated!");
                updateDB();
                txtid.setText(" ");
                txtname.setText(" ");
                txtdaystart.setText(" ");
                txtdayend.setText(" ");
                cbstatus.setSelectedIndex(0);
            }
        } catch (SQLException ex) {
            System.err.println(ex);

        }
    }                                      

