    private void updateActionPerformed(java.awt.event.ActionEvent evt) {                                       
        DefaultTableModel RecordTable=(DefaultTableModel) jTable1.getModel();
        int SelectedRows = jTable1.getSelectedRow();
        try {
            int id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            pst = sqlconn.prepareStatement("update coursedata set courseid=? ,coursename = ? ,daystart=?, dayend=? , status=?where id=?");
            pst.setString(1,txtid.getText());
            pst.setString(2,txtname.getText());
            pst.setString(3,txtdaystart.getText());
            pst.setString(4,txtdayend.getText());
            pst.setString(5,(String) cbstatus.getSelectedItem());
            pst.setInt(6, id);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Course Record Updated!");
            updateDB();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }                                      

