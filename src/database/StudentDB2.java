    private void addnewActionPerformed(java.awt.event.ActionEvent evt) {                                       
        try {
          pst = sqlconn.prepareStatement("insert into studentdata(studentid,firstname,surname,address,gender,mobile,email,status)values"
                  +"(?,?,?,?,?,?,?,?)");
          pst.setString(1,txtstudentid.getText());
          pst.setString(2,txtfirstname.getText());
          pst.setString(3,txtsurname.getText());
          pst.setString(4,txtaddress.getText());
          pst.setString(5,(String) cbgender.getSelectedItem());
          pst.setString(6,txtmobile.getText());
          pst.setString(7,txtemail.getText());
          pst.setString(8,(String) cbstatus.getSelectedItem());
          
          pst.executeUpdate();
          updateDB();
          JOptionPane.showMessageDialog(this,"Student Record Added");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }                                      

