    public  void updateDB(){
        try {
            // kết nối đến database
          pst = sqlconn.prepareStatement("select * from studentdata");
          // lấy data từ bảng studentdata
          rs = pst.executeQuery();
          ResultSetMetaData StData=(ResultSetMetaData) rs.getMetaData();
          int q = StData.getColumnCount();
          DefaultTableModel RecordTable=(DefaultTableModel) jTable1.getModel();
          RecordTable.setRowCount(0);
          while(rs.next()){
              Vector columdata = new Vector();
              for(int i=1;i<=q;i++){
                  columdata.add(rs.getString("id"));
                  columdata.add(rs.getString("studentid"));
                  columdata.add(rs.getString("firstname"));
                  columdata.add(rs.getString("surname"));
                  columdata.add(rs.getString("address"));
                  columdata.add(rs.getString("gender"));
                  columdata.add(rs.getString("mobile"));
                  columdata.add(rs.getString("email"));
                  columdata.add(rs.getString("status"));
              }
              RecordTable.addRow(columdata);
          }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }
